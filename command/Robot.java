package command;

public class Robot {
    
    public void raiseHand() {
        System.out.println("Robot raised hand");
    }

    public void moveFingers() {
        System.out.println("Robot is moving fingers");
    }

    public void moveHandDown() {
        System.out.println("Robot is moving hand down");
    }

    public void walkFrontStep() {
        System.out.println("Robot moved one step ahead");
    }

    public void walkBackStep() {
        System.out.println("Robot moved one step back");
    }
}